let selects = document.querySelectorAll("select");
let btn = document.querySelector("button");
let Input = document.querySelector(".amount input");
let result = document.querySelector(".result");
// let userInput = Input.value;

for (let i = 0; i < selects.length; i++) {
  for (currency_code in country_list) {
    let selected;
    if (i == 0) {
      selected = currency_code == "USD" ? "selected" : "";
    } else if (i == 1) {
      selected = currency_code == "EGP" ? "selected" : "";
    }
    let option = `<option value="${currency_code}" ${selected}>${currency_code}</option>`;
    selects[i].insertAdjacentHTML("beforeend", option);
  }
  selects[i].addEventListener("change", (e) => {
    loadImage(e.target);
  });
}

function loadImage(e) {
  for (code in country_list) {
    if (code == e.value) {
      let imgTag = e.parentElement.querySelector("img");
      imgTag.src = `https://flagsapi.com/${country_list[code]}/flat/64.png`;
    }
  }
}

function getExchange() {
  if (Input.value == "0" || Input.value == "" || Input.value < 0) {
    Input.value = "1";
    Input.value = 1;
  }
}

btn.addEventListener("click", () => {
  getExchange();
  let from = selects[0].options[selects[0].selectedIndex].text;
  let to = selects[1].options[selects[1].selectedIndex].text;
  let apikey = "7643a9379eac5ef74dfef2d6";
  let url = `https://v6.exchangerate-api.com/v6/${apikey}/latest/${from}`;
  fetch(url)
    .then((data) => data.json())
    .then((item) => {
      let exchangerate = item.conversion_rates;
      console.log(exchangerate);
      for (toResult in exchangerate) {
        if (toResult == to) {
          result.innerHTML = `${Input.value} ${from} = ${(
            exchangerate[`${toResult}`] * Input.value
          ).toFixed(2)} ${to}`;
        }
      }
      console.log();
    });
});
